package com.example.quickapp.models;

public class Id{
	private String name;
	private String value;

	public void setName(String name) {
		this.name = name;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getName(){
		return name;
	}

	public String getValue(){
		return value;
	}
}
